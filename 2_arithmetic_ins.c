#include <stdio.h>
int main()
{
    // arithmetic instruction and operator
    int a = 100;
    int b = 10;
    printf("the value of a + b is: %d\n", a + b);
    printf("the value of a - b is: %d\n", a - b);
    printf("the value of a * b is: %d\n", a * b);
    printf("the value of a / b is: %d\n", a / b);
    return 0;
}